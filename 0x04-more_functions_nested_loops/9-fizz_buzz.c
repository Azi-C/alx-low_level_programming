#include <stdio.h>

/**
 * main - prints numbers from 1 to 100
 * prints Fizz for multiples of three
 * prints Buzz for multiples of five
 * prints FizzBuzz for multiples of both three and five
 * Return: Always 0 (success)
 */

int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if (i % 3 == 0 && i % 5 == 0)
			printf("FizzBuzz");
		else if (i % 3 == 0)
			printf("Fizz");
		else if (i % 5 == 0)
			printf("Buzz");
		else
			printf("%i", i);
		if (i < 100)
			printf(" ");
	}
	printf("\n");
	return (0);
}
